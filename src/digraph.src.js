const compress = async () => {
  const blob = new Blob([JSON.stringify(kDigraphList)]);
  const readable = blob.stream().pipeThrough(new CompressionStream("gzip"));

  const buffers = [];
  for await (const data of readable) {
    buffers.push(data);
  }

  const final = Buffer.concat(buffers).toString("base64");

  const f = await fs.open(
    path.resolve(process.cwd(), "./src/digraph.b64.ts"),
    "w"
  );
  await f.write("export const kCompressedDigraphs = `\n");
  let remains = final.length;
  let offset = 0;
  while (remains > 0) {
    const lineSize = Math.min(80, remains);
    const line =
      final.slice(offset, offset + lineSize) + (lineSize == 80 ? "\n" : "`;\n");
    await f.write(line);

    offset += lineSize;
    remains -= lineSize;
  }

  await f.close();
};

